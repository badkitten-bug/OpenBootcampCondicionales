//Caso 1 para ver si un numero es positivo,negativo o cero
public class caso1 {
    public static void main(String[] args) {
        int numeroif = 0;
        if (numeroif > 0) {
            System.out.println("es un numero positivo");
        } else if (numeroif == 0) {
            System.out.println("es cero");
        } else {
            System.out.println("es un numero negativo");
        }
    }
}
